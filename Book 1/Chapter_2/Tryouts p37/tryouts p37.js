// Set the visitor's age
var age = 12;

// Set whether they are with an adult (true/false)
var accompanied = true;

// The OR (||) operator checks if either condition is true:
// 1. Is the age 13 or older? 
// 2. OR is accompanied equal to true?
if (age >= 13 || accompanied === true)
{
    // If either condition is met, this message prints to the console
    console.log("You may enter theatre");
}